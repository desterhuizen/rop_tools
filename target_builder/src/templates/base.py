"""Base server skeleton template.

Generates the shared Winsock2 server structure: includes, main() with
socket setup, accept loop, and thread-per-connection model.
"""

from target_builder.src.config import Architecture, Compiler, ServerConfig


def generate_includes(config: ServerConfig) -> str:
    """Generate C++ #include directives."""
    includes = [
        "#include <winsock2.h>",
        "#include <ws2tcpip.h>",
        "#include <windows.h>",
        "#include <stdio.h>",
        "#include <stdlib.h>",
        "#include <string.h>",
    ]

    if config.dep and config.dep_api.value == "ntallocate":
        includes.append("#include <winternl.h>")

    return "\n".join(includes)


def generate_pragma_comment(config: ServerConfig) -> str:
    """Generate #pragma comment for Winsock library linking."""
    if config.compiler == Compiler.MINGW:
        return "#ifdef _MSC_VER\n" '#pragma comment(lib, "ws2_32.lib")\n' "#endif"
    return '#pragma comment(lib, "ws2_32.lib")'


def generate_compile_instructions(config: ServerConfig) -> str:
    """Generate header comment with compile instructions."""
    arch_label = "x86" if config.arch == Architecture.X86 else "x64"

    if config.compiler == Compiler.MINGW:
        return _mingw_compile_instructions(config, arch_label)
    return _msvc_compile_instructions(config, arch_label)


def _msvc_compile_instructions(config: ServerConfig, arch_label: str) -> str:
    """Generate MSVC compile instructions header comment."""
    prompt_type = (
        "x86 Native Tools" if config.arch == Architecture.X86 else "x64 Native Tools"
    )

    flags = ["/EHsc"]
    if config.stack_canary:
        flags.append("/GS")
    else:
        flags.append("/GS-")

    link_flags = ["ws2_32.lib"]
    if config.dep:
        link_flags.append("/NXCOMPAT")
    else:
        link_flags.append("/NXCOMPAT:NO")
    if config.aslr:
        link_flags.append("/DYNAMICBASE")
    else:
        link_flags.append("/DYNAMICBASE:NO")
    link_flags.append(f"/BASE:0x{config.base_address:08X}")
    if config.arch == Architecture.X86:
        if config.safe_seh:
            link_flags.append("/SAFESEH")
        else:
            link_flags.append("/SAFESEH:NO")

    cl_line = f"cl.exe {' '.join(flags)} server.cpp /link {' '.join(link_flags)}"

    lines = [
        "/*",
        " * Generated by target_builder - FOR AUTHORIZED SECURITY TESTING ONLY",
        f" * Architecture: {arch_label}",
        f" * Compile from: Visual Studio {prompt_type} Command Prompt",
        " *",
        f" *   {cl_line}",
        " */",
    ]
    return "\n".join(lines)


def _mingw_compile_instructions(config: ServerConfig, arch_label: str) -> str:
    """Generate MinGW cross-compile instructions header comment."""
    if config.arch == Architecture.X86:
        compiler = "i686-w64-mingw32-g++"
    else:
        compiler = "x86_64-w64-mingw32-g++"

    cxx_flags = []
    if config.stack_canary:
        cxx_flags.append("-fstack-protector")
    else:
        cxx_flags.append("-fno-stack-protector")

    from target_builder.src.config import VulnType

    if config.vuln_type == VulnType.SEH:
        cxx_flags.append("-fseh-exceptions")

    link_flags = ["-lws2_32", "-static"]
    if config.dep:
        link_flags.append("-Wl,--nxcompat")
    else:
        link_flags.append("-Wl,--disable-nxcompat")
    if config.aslr:
        link_flags.append("-Wl,--dynamicbase")
    else:
        link_flags.append("-Wl,--disable-dynamicbase")
    link_flags.append(f"-Wl,--image-base,0x{config.base_address:08X}")

    flags_str = " ".join(cxx_flags) if cxx_flags else ""
    link_str = " ".join(link_flags)
    compile_line = f"{compiler} {flags_str} server.cpp -o server.exe {link_str}"

    lines = [
        "/*",
        " * Generated by target_builder - FOR AUTHORIZED SECURITY TESTING ONLY",
        f" * Architecture: {arch_label}",
        f" * Compiler: MinGW ({compiler})",
        " *",
        f" *   {compile_line}",
        " *",
        " * To test on Linux: wine ./server.exe",
        " */",
    ]
    return "\n".join(lines)


def generate_globals(config: ServerConfig) -> str:
    """Generate global variables (port, banner, recv buffer size)."""
    banner_escaped = config.banner.replace("\\", "\\\\").replace('"', '\\"')
    return f"""\
#define SERVER_PORT {config.port}
#define RECV_BUF_SIZE 8192
#define BANNER "{banner_escaped}\\n"
"""


def generate_info_leak_function(config: ServerConfig) -> str:
    """Generate a small utility function whose address is leaked via ASLR info leak.

    The attacker sees the leaked pointer, finds this function in the binary
    (e.g. via IDA/Ghidra), and subtracts the offset to compute the EXE base.
    """
    if not config.aslr:
        return ""

    name = config.leak_func_name
    return f"""\
// Internal utility function
int {name}(void) {{
    return SERVER_PORT;
}}
"""


