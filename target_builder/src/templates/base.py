def generate_winsock_init() -> str:
    """Generate WSAStartup / WSACleanup helper."""
    return """\
int init_winsock() {
    WSADATA wsa_data;
    int result = WSAStartup(MAKEWORD(2, 2), &wsa_data);
    if (result != 0) {
        printf("WSAStartup failed: %d\\n", result);
        return 1;
    }
    return 0;
}
"""


def generate_main_function(config: ServerConfig) -> str:
    """Generate main() with socket setup, bind, listen, accept loop."""
    dep_init_call = ""
    if config.dep:
        api = config.dep_api.value
        if api == "virtualprotect":
            dep_init_call = "    init_config_buffer();\n"
        elif api == "virtualalloc":
            dep_init_call = "    init_work_buffer();\n"
        elif api == "heapcreate":
            dep_init_call = "    init_connection_heap();\n"
        elif api == "setprocessdeppolicy":
            dep_init_call = "    check_dep_compat();\n"
        elif api == "ntallocate":
            dep_init_call = "    init_nt_alloc();\n"

    rop_dll_load = ""
    if config.rop_dll.enabled:
        dll_name = config.rop_dll.output_file.replace(".cpp", ".dll")
        rop_dll_load = f"""\
    // Load ROP helper DLL
    HMODULE hRopDll = LoadLibraryA("{dll_name}");
    if (hRopDll) {{
        typedef void (*init_fn)();
        init_fn rop_init = (init_fn)GetProcAddress(hRopDll, "RopHelperInit");
        if (rop_init) rop_init();
        printf("[+] Loaded ROP helper DLL\\n");
    }}

"""

    return f"""\
int main(int argc, char* argv[]) {{
    SOCKET listen_socket = INVALID_SOCKET;
    SOCKET client_socket = INVALID_SOCKET;
    struct sockaddr_in server_addr, client_addr;
    int client_len = sizeof(client_addr);

    printf("[*] %s", BANNER);
    printf("[*] Starting server on port %d...\\n", SERVER_PORT);

    if (init_winsock() != 0) return 1;

{dep_init_call}{rop_dll_load}\
    listen_socket = socket(AF_INET, SOCK_STREAM, IPPROTO_TCP);
    if (listen_socket == INVALID_SOCKET) {{
        printf("[-] Socket creation failed: %d\\n", WSAGetLastError());
        WSACleanup();
        return 1;
    }}

    server_addr.sin_family = AF_INET;
    server_addr.sin_addr.s_addr = INADDR_ANY;
    server_addr.sin_port = htons(SERVER_PORT);

    if (bind(listen_socket, (struct sockaddr*)&server_addr, sizeof(server_addr)) == SOCKET_ERROR) {{
        printf("[-] Bind failed: %d\\n", WSAGetLastError());
        closesocket(listen_socket);
        WSACleanup();
        return 1;
    }}

    if (listen(listen_socket, SOMAXCONN) == SOCKET_ERROR) {{
        printf("[-] Listen failed: %d\\n", WSAGetLastError());
        closesocket(listen_socket);
        WSACleanup();
        return 1;
    }}

    printf("[+] Listening on port %d\\n", SERVER_PORT);

    while (1) {{
        client_socket = accept(listen_socket, (struct sockaddr*)&client_addr, &client_len);
        if (client_socket == INVALID_SOCKET) {{
            printf("[-] Accept failed: %d\\n", WSAGetLastError());
            continue;
        }}

        printf("[+] Connection from %s:%d\\n",
               inet_ntoa(client_addr.sin_addr),
               ntohs(client_addr.sin_port));

        // Handle connection in a new thread
        HANDLE thread = CreateThread(
            NULL, 0,
            (LPTHREAD_START_ROUTINE)handle_connection,
            (LPVOID)client_socket, 0, NULL
        );
        if (thread) {{
            CloseHandle(thread);
        }} else {{
            closesocket(client_socket);
        }}
    }}

    closesocket(listen_socket);
    WSACleanup();
    return 0;
}}"""
